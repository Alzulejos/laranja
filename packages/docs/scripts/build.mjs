// Reads the markdown under content/, parses frontmatter, and emits an importable
// module (dist/index.js + index.d.ts) exporting the parsed pages, a
// section-grouped nav tree, a flat ordered list, and per-page helpers. This is
// the single source of truth any frontend consumes — no file copying.
import { readdirSync, readFileSync, writeFileSync, mkdirSync, rmSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const contentDir = join(root, "content");
const distDir = join(root, "dist");

function parseFrontmatter(src) {
  const m = /^---\r?\n([\s\S]*?)\r?\n---\r?\n?/.exec(src);
  if (!m) return { data: {}, body: src };
  const data = {};
  for (const line of m[1].split(/\r?\n/)) {
    const i = line.indexOf(":");
    if (i === -1) continue;
    const key = line.slice(0, i).trim();
    data[key] = line.slice(i + 1).trim().replace(/^["']|["']$/g, "");
  }
  return { data, body: src.slice(m[0].length) };
}

const slugFromPath = (rel) =>
  rel.replace(/\\/g, "/").replace(/\.md$/, "").replace(/(^|\/)index$/, "");
const sectionOf = (slug) => {
  const i = slug.indexOf("/");
  return i === -1 ? "" : slug.slice(0, i);
};

const files = readdirSync(contentDir, { recursive: true }).filter(
  (f) => typeof f === "string" && f.endsWith(".md"),
);

const pages = files
  .map((rel) => {
    const { data, body } = parseFrontmatter(readFileSync(join(contentDir, rel), "utf8"));
    const slug = slugFromPath(rel);
    return {
      slug,
      section: sectionOf(slug),
      title: data.title ?? slug,
      description: data.description ?? "",
      order: data.order ? Number(data.order) : 999,
      body,
    };
  })
  .sort((a, b) => a.slug.localeCompare(b.slug));

const SECTION_ORDER = ["", "getting-started", "concepts", "configuration", "guides", "cli", "reference"];
const SECTION_TITLES = {
  "": "Overview",
  "getting-started": "Getting started",
  concepts: "Concepts",
  configuration: "Configuration",
  guides: "Guides",
  cli: "CLI",
  reference: "Reference",
};
const byOrder = (a, b) => a.order - b.order || a.title.localeCompare(b.title);

const nav = SECTION_ORDER.map((id) => ({
  id,
  title: SECTION_TITLES[id] ?? id,
  items: pages.filter((p) => p.section === id).sort(byOrder),
})).filter((s) => s.items.length > 0);

const orderedPages = nav.flatMap((s) => s.items);

const runtime = `
const _bySlug = new Map(pages.map((p) => [p.slug, p]));

/** Look up a page by its slug ("" is the docs home). */
export function getPage(slug) {
  return _bySlug.get(slug);
}

/**
 * Build an h2/h3 table of contents whose ids match what rehype-slug emits.
 * Every heading (incl. h1) is fed through the slugger so dedup state stays in
 * lockstep with the rendered DOM; only h2/h3 are surfaced.
 */
export function tableOfContents(body) {
  const slugger = new GithubSlugger();
  const items = [];
  let inFence = false;
  for (const line of body.split(/\\r?\\n/)) {
    if (/^\\s*\`\`\`/.test(line)) {
      inFence = !inFence;
      continue;
    }
    if (inFence) continue;
    const m = /^(#{1,3})\\s+(.*)$/.exec(line);
    if (!m) continue;
    const depth = m[1].length;
    const text = m[2].replace(/\`/g, "").replace(/\\[([^\\]]+)\\]\\([^)]*\\)/g, "$1").trim();
    const id = slugger.slug(text);
    if (depth >= 2) items.push({ depth, text, id });
  }
  return items;
}
`;

const js = `// GENERATED by scripts/build.mjs — do not edit. Run \`npm run build\`.
import GithubSlugger from "github-slugger";

export const pages = ${JSON.stringify(pages)};
export const nav = ${JSON.stringify(nav)};
export const orderedPages = ${JSON.stringify(orderedPages)};
${runtime}`;

const dts = `export interface DocPage {
  /** Route slug relative to the docs root ("" is the home page). */
  slug: string;
  /** Top-level folder ("" for top-level files). */
  section: string;
  title: string;
  description: string;
  order: number;
  /** Markdown body with frontmatter stripped. */
  body: string;
}

export interface NavSection {
  id: string;
  title: string;
  items: DocPage[];
}

export interface TocItem {
  depth: number;
  text: string;
  id: string;
}

export declare const pages: DocPage[];
export declare const nav: NavSection[];
export declare const orderedPages: DocPage[];
export declare function getPage(slug: string): DocPage | undefined;
export declare function tableOfContents(body: string): TocItem[];
`;

rmSync(distDir, { recursive: true, force: true });
mkdirSync(distDir, { recursive: true });
writeFileSync(join(distDir, "index.js"), js);
writeFileSync(join(distDir, "index.d.ts"), dts);

console.log(`@laranja/docs: built ${pages.length} pages → dist/index.js`);
